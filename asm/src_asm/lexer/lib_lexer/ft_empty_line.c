#include "asm.h"

int		ft_empty_line(char *line)
{
	int		i;

	i = 0;
	while (line[i] == ' ' || line[i] == '\t' || line[i] == '\v'
			|| line[i] == '\n')
		i++;
	if (line[i] == 0)
		return (0);
	return (1);
}
