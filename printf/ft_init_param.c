#include "ft_printf.h"

void		ft_init_param(t_param *prm)
{
	prm->p = 0;
	prm->prc = -1;
	prm->wdt = -1;
}

void		ft_init_nd(t_nd *nd)
{
	nd->hi = 0;
	nd->lo = 0;
}
